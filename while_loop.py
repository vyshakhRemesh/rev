#while condition:
#will execute the block of code inside while loop aslong as the condition is true


i=1
while i<=5: #will execute the block of code inside as long as i<=5
    print(i)
    i=i+1
print('done')

while i<=10: #will execute the block of code inside as long as i<=5
    print('*'*i)
    i=i+1
print('done')