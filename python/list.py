names=['john','bob','mosh','sarah','mary']
print(names)
print(names[2]) #will print the element in the 2nd index position in the list
print(names[-2]) #will print the second element from the end
print(names[1:3]) #will print the elements in the range
print(names[:3])
print(names[1:])
#it is just like string
names[0]='vyshakh' #replace the element in the 0th index position
print(names)
